import math
#Modulo Math.ceil. Redonda o valor floar para cima
print ("Utilizando Math.ceil\n",math.ceil(4.1))
#Result = 5

#Math.floor redonda para baixo.
print("Utilizando math.floor\n", math.floor(3.9))
#Result = 3

#math.fsum Soma todos valor de uma lista
print("Utilizando math.fsum\n", math.fsum([1,2,3,4,5,6,7]))
#Result = 28

#math.sqrt() calcula a raiz quadrada
print("Utilizando math.sqrt\n", math.sqrt(16))
#result = 4

